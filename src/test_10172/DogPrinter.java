package test_10172;

public class DogPrinter {
    public static void main(String[] args) {

        System.out.println("|\\_/|");
        System.out.println("|q p|   /}");
        System.out.println("( 0 )\"\"\"\\");
        System.out.println("|\"^\"`    |");
        System.out.println("||_/=\\\\__|");

    }
}

/**
 * [자바 이스케이프 문자(Escape Sequence) 요약]
 * * \n  : 줄 바꿈 (New Line) -> 커서를 다음 줄로 이동
 * \t  : 탭 (Tab)           -> 일정 간격(공백) 띄우기
 * \\  : 백슬래시 (\)       -> 백슬래시 문자 자체를 출력
 * \"  : 큰따옴표 (")       -> 문자열 안에서 큰따옴표를 문자로 출력
 * \'  : 작은따옴표 (')     -> 문자열 안에서 작은따옴표를 문자로 출력 (주로 char 타입에서 사용)
 */